import turtle

turtle.speed(1)
turtle.shape("turtle")#circle   triangle arrow classic turtle
turtle.forward(100)
turtle.goto(-100,200)
turtle.right(90)
turtle.forward(200)
turtle.left(90)
turtle.penup()
turtle.goto(100,200)
turtle.backward(200)

turtle.pendown()
turtle.goto(-200,0)
turtle.forward(200)
turtle.hideturtle()
turtle.done()
