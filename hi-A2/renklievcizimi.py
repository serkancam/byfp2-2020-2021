import turtle

turtle.Screen().setup(800,600)
turtle.pensize(4)
turtle.shape("turtle")
turtle.color("red","green")
turtle.penup()
turtle.goto(-200,-200)
turtle.pendown()
turtle.begin_fill()
turtle.forward(400)
turtle.left(90)
turtle.forward(200)
turtle.left(90)
turtle.forward(400)
turtle.left(90)
turtle.forward(200)
turtle.left(90)
turtle.end_fill()
turtle.penup()
turtle.goto(-200,0)
turtle.pendown()
turtle.color("red","yellow")
turtle.begin_fill()
turtle.forward(400)
turtle.left(135)
turtle.forward(283)
turtle.left(90)
turtle.forward(283)
turtle.left(135)
turtle.end_fill()
turtle.penup()
turtle.goto(-30,-200)
turtle.pendown()
turtle.color("red","brown")
turtle.begin_fill()
turtle.forward(60)
turtle.left(90)
turtle.forward(100)
turtle.left(90)
turtle.forward(60)
turtle.left(90)
turtle.forward(100)
turtle.left(90)
turtle.end_fill()

turtle.hideturtle()
